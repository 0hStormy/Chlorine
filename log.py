def info(message: str):
    """
    Prints info message
    
    :param message: Message to include
    :type message: str
    """
    print(f"INFO: {message}")

def warn(message: str):
    """
    Prints warning message
    
    :param message: Message to include
    :type message: str
    """
    print(f"WARN: {message}")

def error(message: str):
    """
    Prints error message
    
    :param message: Message to include
    :type message: str
    """
    print(f"ERROR: {message}")
